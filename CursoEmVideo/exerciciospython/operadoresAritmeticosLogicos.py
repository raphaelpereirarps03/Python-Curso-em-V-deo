# ARITMÉTICOS:
#
# Soma = + : n1 + n2 = x
# Subtração = - : n1 - n2 = x
# Multiplicação = * : n1 * n2 = x
# Divisão = / : n1 / n2 = x
# Potenciação = ** : n1 ** n2 = x
# Divisão inteira = // : n1 // n2 = x
# Módulo = % : n1 % n2 = x
#
#-----------------------------------------
# ORDEM DE PRECEDÊNCIA DOS ARITMÉTICOS:
#
# 1 - ();
# 2 - **;
# 3 - *, /, //, %;
# 4 - +, -.
#
#-----------------------------------------
# LÓGICOS:
#
# Igual à : ==
# Diferente de : !=
# Menor que : <
# Maior que : >
# Menor ou igual que : <=
# Maior ou igual que : >=
#
#-----------------------------------------
# OBS: Lógica para calcular a raiz de um núméro:
# x ** (1/y)
# x : número que queremos saber a raíz
# y : a raíz desejada.
# Então:
# 81 ** (1/2) == 9
# A raíz quadrada (2) de 81 é igual à 9 :)
